import axios from 'axios';
const base = 'https://marine-api.open-meteo.com/v1/marine';

export async function getMarine(lat, lon) {
  const params = {
    latitude: lat,
    longitude: lon,
    hourly: [
      'wave_height',
      'swell_wave_height',
      'wind_speed_10m',
      'sea_surface_temperature',
      'tide_height',
    ].join(','),
    timezone: 'auto',
  };
  const { data } = await axios.get(base, { params });
  return data;
}
